class Person
{
    constructor(name,age)
    {
        this.age=age;
        this.name=name;
    }
    getName()
    {
        return this.name;
    }
    getAge()
    {
        return this.age;
    }
}
class Student
{
    constructor(name,age,year,department)
    {
        this.year=year;
        this.department=department;
    }
}
